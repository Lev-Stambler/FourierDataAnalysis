"""On-policy dataset generation for compressing a masked-diffusion LM (Dream) via dataset-CSAMP.

One model plays every role: the function f to compress is Dream's masked next-token distribution,
and the CSAMP fibers are generated by the SAME model infilling window positions conditioned on the
fiber's fixed content (two infills of one fiber = a conditional-sample pair ~ P(g|s)).

LONG NATURAL CONTEXT (>= 128 tokens): each fiber is anchored at a real story position -- the
`ctx_len - w` REAL tokens preceding the window are the fiber's prefix, the last k window tokens are
the fixed string s, and Dream infills only the w-k free window positions between them.  Fixed
strings are DEDUPED so each distinct s belongs to exactly one fiber/prefix; hence
f(window) = Dream(next | prefix(s), g, s) is a well-defined function of the window on the dataset,
and every dataset-GL guarantee applies to the w-token window unchanged.  Dream always sees
story-shaped input of ctx_len tokens (native regime), while the GL domain stays int64-packable.

Native-to-Dream details: Dream is initialized from an AR model, so its output at position i
predicts token i+1 -- ALL logits are right-shifted (as in Dream's own generation_utils) before use;
infilling unmasks in confidence order (prob of the sampled token, maskgit_plus-style).

Only this module touches `transformers`.  Everything model-free (collapse, splits, projection,
fiber drawing) is pure numpy so the pipeline is testable on CPU without weights.
"""

from __future__ import annotations

import numpy as np
import torch

from .hf_data import _pack_unique

DREAM = "Dream-org/Dream-v0-Base-7B"
_FIRST_SPECIAL = 151643                      # Qwen2.5 specials start here; never eligible as slots


# --------------------------------------------------------------------------------------- pure numpy

def soft_collapse(C):
    """DISTINCT generated contexts + multiplicities: (Cd (D,w), n_ctx (D,), inv (m,), m).
    Dedupe BEFORE labeling so the model is queried once per distinct context."""
    Cd, inv = _pack_unique(np.asarray(C, dtype=np.int64))
    return Cd, np.bincount(inv, minlength=len(Cd)).astype(np.int64), inv, len(C)


def fiber_disjoint_split(fiber_id, val_frac=0.15, seed=0):
    """Assign each FIBER wholly to train or val (analog of bpe_lm's story-disjoint split).
    Returns (train_mask, val_mask) over the raw context rows."""
    fiber_id = np.asarray(fiber_id)
    fibers = np.unique(fiber_id)
    rng = np.random.default_rng(seed)
    val_fibers = set(fibers[rng.random(len(fibers)) < val_frac].tolist())
    va = np.isin(fiber_id, list(val_fibers))
    return ~va, va


def project_to_slots(full, slot_ids):
    """(D, vocab) full-vocab distribution -> (D, V) slot distribution: slot v>=1 takes the prob of
    Qwen id slot_ids[v]; slot 0 = OTHER = the residual mass.  Rows keep summing to 1."""
    full = np.asarray(full, dtype=np.float64)
    P = np.empty((len(full), len(slot_ids)), dtype=np.float64)
    P[:, 1:] = full[:, slot_ids[1:]]
    P[:, 0] = np.maximum(1.0 - P[:, 1:].sum(1), 0.0)              # float roundoff can dip below 0
    return P


def slots_from_mass(mass, V):
    """slot_ids (V,) from a full-vocab mass vector: slots 1..V-1 = heaviest non-special ids,
    slot 0 = OTHER sentinel (-1)."""
    mass = np.asarray(mass, dtype=np.float64).copy()
    mass[_FIRST_SPECIAL:] = -1.0
    top = np.argsort(-mass)[: V - 1]
    return np.concatenate([[-1], top]).astype(np.int64)


def make_tok2slot(slot_ids, vocab_size):
    """Inverse map Qwen id -> slot (0 for OTHER)."""
    t2s = np.zeros(vocab_size, dtype=np.int64)
    t2s[slot_ids[1:]] = np.arange(1, len(slot_ids))
    return t2s


def draw_fibers(streams, w, k, ctx_len, M, allowed_ids, seed=0, dedupe=True):
    """M fiber anchors from tokenized stories: positions t with >= ctx_len preceding tokens whose
    last w window tokens AND next token are all in `allowed_ids`.  Returns
    (PRE (M, ctx_len-w) real prefix ids, S_tok (M, k) fixed strings, WIN (M, w) the REAL window
    tokens, y_tok (M,) the real next token).  `dedupe` keeps one anchor per distinct s so
    f(window) = Dream(next | prefix(s), ...) is well-defined; WIN/y_tok serve real-window eval."""
    hi = int(max(int(max(s.max() for s in streams if len(s))), int(allowed_ids.max()))) + 1
    ok = np.zeros(hi, bool)
    ok[allowed_ids] = True
    from numpy.lib.stride_tricks import sliding_window_view
    anchors = []
    for si, ids in enumerate(streams):
        ids = np.asarray(ids, dtype=np.int64)
        if len(ids) < ctx_len + 1:
            continue
        win_ok = sliding_window_view(ok[ids], w + 1).all(axis=1)  # j -> ids[j:j+w+1], anchor t = j+w
        ts = np.nonzero(win_ok)[0] + w
        anchors.extend((si, int(t)) for t in ts[(ts >= ctx_len) & (ts < len(ids))])
    if not anchors:
        raise ValueError("no in-alphabet anchors with enough preceding context")
    rng = np.random.default_rng(seed)
    anchors = [anchors[i] for i in rng.permutation(len(anchors))]
    PRE, S, WIN, Y, seen = [], [], [], [], set()
    for si, t in anchors:
        ids = np.asarray(streams[si], dtype=np.int64)
        s_key = tuple(ids[t - k:t])
        if dedupe and s_key in seen:
            continue
        seen.add(s_key)
        PRE.append(ids[t - ctx_len:t - w])
        WIN.append(ids[t - w:t])
        S.append(ids[t - k:t])
        Y.append(ids[t])
        if len(S) == M:
            break
    if len(S) < M:
        raise ValueError(f"only {len(S)} distinct in-alphabet fibers available, need {M}")
    return (np.stack(PRE).astype(np.int64), np.stack(S).astype(np.int64),
            np.stack(WIN).astype(np.int64), np.asarray(Y, dtype=np.int64))


# ------------------------------------------------------------------------------------- model access

def load_dream(name=DREAM, device=None, dtype=torch.bfloat16):
    """Dream + tokenizer (trust_remote_code).  Returns (model, tokenizer, mask_id)."""
    from transformers import AutoModel, AutoTokenizer
    device = device or ("cuda" if torch.cuda.is_available() else "cpu")
    tok = AutoTokenizer.from_pretrained(name, trust_remote_code=True)
    model = AutoModel.from_pretrained(name, torch_dtype=dtype, trust_remote_code=True).to(device).eval()
    return model, tok, tok.mask_token_id


@torch.no_grad()
def _pred_logits(model, ids, lo, hi):
    """Logits PREDICTING positions lo..hi-1 of `ids`.  Dream is AR-initialized: raw output at
    position i predicts token i+1 (its own generation_utils right-shifts all logits), so the
    prediction for position j is raw logits[:, j-1].  Slicing before any conversion keeps the
    full-vocab tensor small.  Requires lo >= 1 (always true: a real prefix precedes the window)."""
    out = model(input_ids=ids)
    logits = out.logits if hasattr(out, "logits") else out[0]
    return logits[:, lo - 1:hi - 1]


@torch.no_grad()
def diffusion_infill(model, PRE, S_tok, w, allowed_ids, mask_id, R=8, steps=None, temperature=1.0,
                     batch=64, seed=0, device="cpu"):
    """On-policy fiber generation: for each fiber (real prefix PRE[i], fixed string S_tok[i]), R
    infills of the first w-k window positions, conditioned on [PRE, <masks>, s], restricted to
    `allowed_ids`, unmasked in confidence order (prob of the sampled token) over `steps` rounds
    (default one per position).  Returns (C_tok (M*R, w) int64 windows in text order [g..., s...],
    fiber_id (M*R,))."""
    PRE = np.asarray(PRE, dtype=np.int64)
    S_tok = np.asarray(S_tok, dtype=np.int64)
    M, k = S_tok.shape
    fill, p = w - k, PRE.shape[1]
    steps = steps or fill
    allowed_t = torch.as_tensor(np.asarray(allowed_ids, dtype=np.int64), device=device)
    gen = torch.Generator(device="cpu").manual_seed(seed)
    fiber_id = np.repeat(np.arange(M), R)
    out = np.empty((M * R, w), dtype=np.int64)
    for lo in range(0, M * R, batch):
        rows = fiber_id[lo:lo + batch]
        B = len(rows)
        x = torch.full((B, p + w), mask_id, dtype=torch.int64, device=device)
        x[:, :p] = torch.as_tensor(PRE[rows], device=device)
        x[:, p + fill:] = torch.as_tensor(S_tok[rows], device=device)
        todo = torch.full((B,), fill, dtype=torch.int64, device=device)
        for step in range(steps):
            quota = (todo + (steps - 1 - step)) // (steps - step)                   # ceil(todo/steps left)
            win = x[:, p:p + fill]                                                  # the free positions
            masked = win == mask_id                                                 # (B, fill)
            lg = _pred_logits(model, x, p, p + fill)[:, :, allowed_t].float()       # (B, fill, |allowed|)
            pr = torch.softmax(lg / max(temperature, 1e-6), dim=-1)
            flat = pr.view(-1, pr.shape[-1])
            pick = torch.multinomial(flat.cpu(), 1, generator=gen).to(device).view(B, -1)   # (B, fill)
            conf = pr.gather(-1, pick[:, :, None])[:, :, 0]                          # prob of the sample
            conf = torch.where(masked, conf, torch.full_like(conf, -1.0))
            order = torch.argsort(conf, dim=1, descending=True)
            rank = torch.empty_like(order)
            rank.scatter_(1, order, torch.arange(fill, device=device).expand(B, -1))
            unmask = masked & (rank < quota[:, None])
            x[:, p:p + fill] = torch.where(unmask, allowed_t[pick], win)
            todo = todo - unmask.sum(1)
        assert not (x == mask_id).any()
        out[lo:lo + batch] = x[:, p:].cpu().numpy()
        if (lo // batch) % 50 == 0:
            print(f"[infill] {lo + B}/{M * R}", flush=True)
    return out, fiber_id


@torch.no_grad()
def label_model(model, PRE_rows, C_tok, mask_id, slot_ids, batch=64, device="cpu"):
    """f(x): forward on [PRE_rows[i], C_tok[i], <mask>], softmax at the mask, projected onto slots.
    PRE_rows (D, p) is each context's fiber prefix (real story tokens).  Returns P (D, V) float32."""
    PRE_rows = np.asarray(PRE_rows, dtype=np.int64)
    C_tok = np.asarray(C_tok, dtype=np.int64)
    P = np.empty((len(C_tok), len(slot_ids)), dtype=np.float64)
    for lo in range(0, len(C_tok), batch):
        ids = np.concatenate([PRE_rows[lo:lo + batch], C_tok[lo:lo + batch],
                              np.full((len(C_tok[lo:lo + batch]), 1), mask_id)], axis=1)
        ids_t = torch.as_tensor(ids, device=device)
        lg = _pred_logits(model, ids_t, ids.shape[1] - 1, ids.shape[1])[:, 0].float()   # (B, vocab)
        P[lo:lo + batch] = project_to_slots(torch.softmax(lg, dim=-1).cpu().numpy(), slot_ids)
    return P.astype(np.float32)


@torch.no_grad()
def build_top_vocab(model, tok, texts, V=512, mask_id=None, n_windows=2000, ctx_len=128, seed=0,
                    batch=64, device="cpu"):
    """Slot alphabet by MODEL probability mass: mean next-token distribution at random positions of
    real stories, each with ctx_len real preceding tokens.  Returns (slot_ids (V,), streams)."""
    rng = np.random.default_rng(seed)
    streams = [np.asarray(tok(t, add_special_tokens=False)["input_ids"], np.int64) for t in texts]
    long = [s for s in streams if len(s) > ctx_len + 1]
    wins = []
    for _ in range(n_windows):
        s = long[rng.integers(len(long))]
        i = rng.integers(ctx_len, len(s))
        wins.append(s[i - ctx_len:i])
    wins = np.stack(wins)
    mass = np.zeros(model.config.vocab_size, dtype=np.float64)
    for lo in range(0, len(wins), batch):
        ids = np.concatenate([wins[lo:lo + batch],
                              np.full((len(wins[lo:lo + batch]), 1), mask_id)], axis=1)
        ids_t = torch.as_tensor(ids, device=device)
        lg = _pred_logits(model, ids_t, ids.shape[1] - 1, ids.shape[1])[:, 0].float()
        mass += torch.softmax(lg, dim=-1).sum(0).cpu().numpy()
    return slots_from_mass(mass / len(wins), V), streams
